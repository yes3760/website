    Ben YUNUS EMRE SEVİNDİK, Mersin'in Erdemli ilçesinde doğmuş, Silifke'de büyümüş bir 
    bilgisayar mühendisliği öğrencisiyim. Gerek çocukluğumun geçtiği Silifke, gerekse şu anki yaşadığım 
    Erdemli, benim için anılarla dolu ve özel bir yer taşıyor. Doğduğum topraklarda annem Gülperi ve 
    babam Mehmet ile birlikte mutlu bir çocukluk geçirdim. Ailem, benim hayatımda önemli bir 
    destek ve ilham kaynağı olmuştur.
     İlköğrenimime Silifke'de başladım ve Erdemli Anadolu Lisesi'nde devam ettim. Okul 
    yıllarımda, öğrenmeye ve kendimi geliştirmeye olan ilgim her zaman yüksekti. Lise eğitimim 
    boyunca, özellikle matematik ve bilgisayar bilimleri alanında gösterdiğim performansla, 
    okulumun en iyi 10 öğrencisi arasında yer almayı başardım. Bu başarı, beni bilim ve 
    teknolojiye duyduğum ilgiyi daha da pekiştirdi.
     Ailemden aldığım destekle, üniversite hayalime adım atmaya karar verdim ve bilgisayar 
    mühendisliği alanında eğitim almaya başladım. Çalışkanlığım ve azmim sayesinde bu 
    alandaki bilgi ve becerilerimi sürekli olarak geliştirmeye çabalıyorum. Teknolojinin hızla 
    ilerlediği günümüzde, kendimi sürekli güncel tutmak ve sektördeki yenilikleri takip etmek, 
    benim için vazgeçilmez bir prensip haline geldi.
     Kardeşlerimden biri Çukurova Tıp Fakültesi'nde eğitimine devam ederken diğeri lise 11. 
    sınıfta olan ,iki kardeşim bulunmaktadır. Bu durum, ailemizin eğitime verdiği değeri 
    göstermektedir. Ailemin ve kendi çabalarımın bir sonucu olarak, mühendislik alanındaki 
    kariyerimde başarılı olmak için çabalıyor ve her geçen gün daha fazla öğrenmeye, keşfetmeye 
    odaklanıyorum.
     Erdemli Anadolu Lisesi mezunu olarak, o dönemde kazandığım bilgi birikimi ve öğrenme 
    tutkusu, beni bugünkü noktaya getirdi. Hedefim, bilgisayar mühendisliği alanında başarılı bir 
    profesyonel olmak ve teknolojinin geleceğine yön vermek. Bu doğrultuda, hem akademik 
    hem de kişisel olarak kendimi geliştirme yolunda adımlar atmaya devam ediyorum





