            I. Dünya Savaşında İtilaf Devletlerinin işgaline uğrayan Mersin, Milli Mücadele ile 3 Ocak 1922’de tekrar Türk hâkimiyetine girmiştir. 1924 yılında Mersin Adıyla Vilayet olmuş, 1933 yılında da Merkezi Silifke olan İçel ile birleştirilerek İçel adını almıştır. 28 Haziran 2002 tarihli Resmi Gazete’de yayımlanan 4764 sayılı Kanunla İl’in ismi yeniden Mersin olmuştur.
